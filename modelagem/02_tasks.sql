DECLARE
    V_COUNT NUMBER;
BEGIN
    SELECT COUNT(*)
      INTO V_COUNT
      FROM ALL_TABLES
     WHERE OWNER = 'TODO_LIST'
       AND TABLE_NAME = 'TAREFA_STATUS';

    IF V_COUNT = 0 THEN
        EXECUTE IMMEDIATE q'[
            CREATE TABLE TODO_LIST.TAREFA_STATUS (
                ID NUMBER NOT NULL,
                CODIGO VARCHAR2(50 CHAR) NOT NULL,
                DESCRICAO VARCHAR2(100 CHAR) NOT NULL,
                ATIVO NUMBER(1) DEFAULT 1 NOT NULL,
                CONSTRAINT PK_TAREFA_STATUS PRIMARY KEY (ID),
                CONSTRAINT UK_TAREFA_STATUS_CODIGO UNIQUE (CODIGO),
                CONSTRAINT CK_TAREFA_STATUS_ATIVO CHECK (ATIVO IN (0, 1))
            )
        ]';
    END IF;
END;
/

MERGE INTO TODO_LIST.TAREFA_STATUS DESTINO
USING (
    SELECT 1 AS ID, 'PENDENTE' AS CODIGO, 'Pendente' AS DESCRICAO, 1 AS ATIVO FROM DUAL
    UNION ALL
    SELECT 2 AS ID, 'EM_ANDAMENTO' AS CODIGO, 'Em andamento' AS DESCRICAO, 1 AS ATIVO FROM DUAL
    UNION ALL
    SELECT 3 AS ID, 'CONCLUIDA' AS CODIGO, 'Concluida' AS DESCRICAO, 1 AS ATIVO FROM DUAL
    UNION ALL
    SELECT 4 AS ID, 'CANCELADA' AS CODIGO, 'Cancelada' AS DESCRICAO, 1 AS ATIVO FROM DUAL
) ORIGEM
ON (DESTINO.CODIGO = ORIGEM.CODIGO)
WHEN MATCHED THEN
    UPDATE SET DESTINO.DESCRICAO = ORIGEM.DESCRICAO,
               DESTINO.ATIVO = ORIGEM.ATIVO
WHEN NOT MATCHED THEN
    INSERT (ID, CODIGO, DESCRICAO, ATIVO)
    VALUES (ORIGEM.ID, ORIGEM.CODIGO, ORIGEM.DESCRICAO, ORIGEM.ATIVO);

