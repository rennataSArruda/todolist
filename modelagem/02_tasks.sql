CREATE OR REPLACE VIEW VW_TAREFA AS
SELECT
    T.ID,

    -- USUARIO
    T.USUARIO_ID,

    -- CATEGORIA
    T.CATEGORIA_ID,
    C.NOME AS CATEGORIA_NOME,
    C.DESCRICAO AS CATEGORIA_DESCRICAO,

    -- STATUS
    T.STATUS_ID,
    S.CODIGO AS STATUS_CODIGO,
    S.DESCRICAO AS STATUS_DESCRICAO,

    -- PRIORIDADE
    T.PRIORIDADE_ID,
    P.CODIGO AS PRIORIDADE_CODIGO,
    P.DESCRICAO AS PRIORIDADE_DESCRICAO,
    P.ORDEM AS PRIORIDADE_ORDEM,

    -- DADOS DA TAREFA
    T.TITULO,
    T.DESCRICAO,

    T.DATA_INICIO,
    T.DATA_FIM,
    T.DATA_CONCLUSAO,

    T.POSICAO,
    T.IMPORTANTE,
    T.ATIVO,

    T.CREATED_AT,
    T.UPDATED_AT

FROM TAREFA T

         LEFT JOIN TAREFA_CATEGORIA C
                   ON C.ID = T.CATEGORIA_ID

         INNER JOIN TAREFA_STATUS S
                    ON S.ID = T.STATUS_ID

         INNER JOIN TAREFA_PRIORIDADE P
                    ON P.ID = T.PRIORIDADE_ID;

commit ;
