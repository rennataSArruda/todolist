DECLARE
    V_TABLE_EXISTS NUMBER;
    V_TOKEN_COLUMN_EXISTS NUMBER;
    V_TOKEN_HASH_COLUMN_EXISTS NUMBER;
BEGIN
    SELECT COUNT(*)
      INTO V_TABLE_EXISTS
      FROM ALL_TABLES
     WHERE OWNER = 'TODO_LIST'
       AND TABLE_NAME = 'REFRESH_TOKEN';

    IF V_TABLE_EXISTS > 0 THEN
        SELECT COUNT(*)
          INTO V_TOKEN_COLUMN_EXISTS
          FROM ALL_TAB_COLUMNS
         WHERE OWNER = 'TODO_LIST'
           AND TABLE_NAME = 'REFRESH_TOKEN'
           AND COLUMN_NAME = 'TOKEN';

        SELECT COUNT(*)
          INTO V_TOKEN_HASH_COLUMN_EXISTS
          FROM ALL_TAB_COLUMNS
         WHERE OWNER = 'TODO_LIST'
           AND TABLE_NAME = 'REFRESH_TOKEN'
           AND COLUMN_NAME = 'TOKEN_HASH';

        IF V_TOKEN_COLUMN_EXISTS > 0 AND V_TOKEN_HASH_COLUMN_EXISTS = 0 THEN
            EXECUTE IMMEDIATE 'ALTER TABLE TODO_LIST.REFRESH_TOKEN RENAME COLUMN TOKEN TO TOKEN_HASH';
        END IF;

        EXECUTE IMMEDIATE q'[
            UPDATE TODO_LIST.REFRESH_TOKEN
               SET TOKEN_HASH = LOWER(RAWTOHEX(STANDARD_HASH(TOKEN_HASH, 'SHA256')))
             WHERE TOKEN_HASH IS NOT NULL
               AND LENGTH(TOKEN_HASH) != 64
        ]';

        EXECUTE IMMEDIATE 'ALTER TABLE TODO_LIST.REFRESH_TOKEN MODIFY (TOKEN_HASH VARCHAR2(64 CHAR) NOT NULL)';
    END IF;
END;
